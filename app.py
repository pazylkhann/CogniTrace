"""
CogniTrace - Анализ ошибок рассуждений учащихся
Профессиональное приложение для анализа ошибок в STEM и гуманитарных предметах
с использованием сократического метода обучения
"""

import streamlit as st
from google import genai

# ============================================================================
# КОНФИГУРАЦИЯ СТРАНИЦЫ
# ============================================================================
st.set_page_config(
    page_title="CogniTrace",
    page_icon="🚀",
    layout="wide",
    initial_sidebar_state="expanded"
)


# ============================================================================
# ФУНКЦИЯ НАСТРОЙКИ API
# ============================================================================
GEMINI_MODEL = "gemini-2.0-flash"


def setup_gemini_client():
    """
    Создает клиент Google GenAI.
    Проверяет наличие API ключа в секретах Streamlit.
    """
    try:
        api_key = st.secrets["GEMINI_API_KEY"]
        return genai.Client(api_key=api_key)
    except KeyError:
        st.warning("⚠️ API ключ не найден. Пожалуйста, добавьте GEMINI_API_KEY в секреты Streamlit.")
        st.info("💡 Для локальной разработки создайте файл `.streamlit/secrets.toml` и добавьте: GEMINI_API_KEY = 'ваш_ключ'")
        return None
    except Exception as e:
        st.error(f"Ошибка при настройке API: {str(e)}")
        return None


# ============================================================================
# ФУНКЦИЯ СОЗДАНИЯ ПРОМПТА
# ============================================================================
def create_analysis_prompt(task_description: str, student_answer: str) -> str:
    """
    Создает промпт для анализа ответа ученика с использованием сократического метода.
    
    Args:
        task_description: Описание задачи от учителя
        student_answer: Ответ ученика для анализа
        
    Returns:
        Полный промпт для отправки в AI модель
    """
    prompt = f"""Ты - опытный педагог, использующий сократический метод обучения.

ЗАДАЧА ОТ УЧИТЕЛЯ:
{task_description}

ОТВЕТ УЧЕНИКА:
{student_answer}

ПРОАНАЛИЗИРУЙ ОТВЕТ УЧЕНИКА И ВЫПОЛНИ СЛЕДУЮЩЕЕ:

1. ИДЕНТИФИЦИРУЙ конкретную когнитивную преграду или логическую ошибку в рассуждении ученика.

2. ОБЪЯСНИ ПОЭТАПНО, ПОЧЕМУ логика ученика неверна. Разбери шаг за шагом, где именно произошел сбой в рассуждении.

3. ВАЖНО: НЕ давай сразу правильный ответ. Не раскрывай решение полностью.

4. В КОНЦЕ задай "Сократический вопрос" - наводящий вопрос, который поможет ученику самостоятельно прийти к правильному выводу.

Твой ответ должен быть:
- Четким и структурированным
- Поддерживающим, но не снисходительным
- Фокусированным на процессе мышления, а не только на ответе
- Написанным на русском языке

Начни анализ:"""
    
    return prompt


# ============================================================================
# ФУНКЦИЯ АНАЛИЗА ОТВЕТА
# ============================================================================
def analyze_answer(client, task_description: str, student_answer: str) -> str:
    """
    Отправляет запрос к AI модели для анализа ответа ученика.
    
    Args:
        client: Клиент Google GenAI
        task_description: Описание задачи от учителя
        student_answer: Ответ ученика
        
    Returns:
        Результат анализа от AI
    """
    prompt = create_analysis_prompt(task_description, student_answer)
    try:
        response = client.models.generate_content(
            model=GEMINI_MODEL,
            contents=prompt,
        )
        return response.text
    except Exception as e:
        raise Exception(f"Ошибка при обращении к AI модели: {str(e)}")


# ============================================================================
# БОКОВАЯ ПАНЕЛЬ
# ============================================================================
with st.sidebar:
    st.title("📚 О CogniTrace")
    st.markdown("---")
    st.markdown("""
    **CogniTrace** - это инструмент для анализа **мышления, а не просто ответов**.
    
    ### Как это работает:
    - Анализирует **когнитивные преграды** в рассуждениях ученика
    - Выявляет **логические ошибки** на каждом шаге
    - Использует **сократический метод** для направляющего обучения
    - Помогает ученикам прийти к пониманию самостоятельно
    
    ### Применение:
    - STEM предметы (математика, физика, информатика)
    - Гуманитарные дисциплины (литература, история, философия)
    - Любые задачи, требующие логического рассуждения
    """)
    st.markdown("---")
    st.caption("Версия 1.0 | Powered by Gemini 2.0")


# ============================================================================
# ОСНОВНОЙ ИНТЕРФЕЙС
# ============================================================================
st.title("🚀 CogniTrace")
st.markdown("### Анализ ошибок рассуждений с использованием сократического метода")
st.markdown("---")

client = setup_gemini_client()

# Инициализация session_state для сохранения результатов
if "analysis_result" not in st.session_state:
    st.session_state.analysis_result = None

st.subheader("Входные данные")

# Текстовое поле для описания задачи (большое поле для ввода)
task_description = st.text_area(
    "Описание задачи (от учителя)",
    height=200,
    placeholder="Введите описание задачи или вопроса, который был задан ученику...",
    help="Опишите задачу, которую должен решить ученик",
    key="task_description"
)

# Текстовое поле для ответа ученика (большое поле для ввода)
student_answer = st.text_area(
    "Ответ ученика",
    height=200,
    placeholder="Введите ответ ученика для анализа...",
    help="Вставьте ответ ученика, который нужно проанализировать",
    key="student_answer"
)

# Кнопка анализа
analyze_button = st.button(
    "🔍 Анализировать",
    use_container_width=True,
    type="primary"
)

# Обработка нажатия кнопки анализа
if analyze_button:
    # Валидация входных данных
    if not task_description.strip():
        st.warning("⚠️ Пожалуйста, введите описание задачи.")
    elif not student_answer.strip():
        st.warning("⚠️ Пожалуйста, введите ответ ученика.")
    elif client is None:
        st.warning("⚠️ API ключ не настроен. Невозможно выполнить анализ.")
    else:
        with st.spinner("🤔 Анализирую рассуждения ученика... Это может занять несколько секунд."):
            try:
                analysis_result = analyze_answer(client, task_description, student_answer)
                
                # Сохраняем результат в session_state
                st.session_state.analysis_result = analysis_result
                
            except Exception as e:
                # Обработка ошибок при запросе к API
                st.session_state.analysis_result = None
                st.error(f"❌ Произошла ошибка при анализе: {str(e)}")
                st.info("💡 Проверьте подключение к интернету и настройки API ключа.")

# Отображаем результат анализа (если есть)
if st.session_state.analysis_result:
    st.markdown("---")
    st.subheader("📊 Результат анализа")
    st.info(st.session_state.analysis_result)

# Футер с подсказкой
st.markdown("---")
st.caption("💡 Совет: Чем более подробно описана задача и ответ ученика, тем точнее будет анализ.")
